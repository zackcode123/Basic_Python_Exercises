'''Faça um programa que pergunte quanto você ganha por hora
e o número de horas trabalhadas no mês. Calcule e mostre o total
do seu salário no referido mês.'''

n1 = int(input('Quanto você ganha por hora? '))
n2 = int(input('Quanto horas você trabalha por mês? '))
soma = (n1*n2)
print('O seu salario mensal eaquivale à R$',soma,'!')
